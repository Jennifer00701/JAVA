// 자료형 

// psvm / main -> 자동 

public class _02_DataTypes {
    public static void main(String[] args) { // psvm / main -> 자동 
     System.out.println("ㄹㄹ"); // sout -> 자동
     System.out.println("안녕"); // 문자열
     System.out.println(10); // 정수
     System.out.println(-12); // 음수
     System.out.println(2.13); // 실수
     System.out.println(true); //스위치온
     System.out.println(false); //스위치 아웃 / 불리언
     System.out.println(12+12); 
    } 
 }
 